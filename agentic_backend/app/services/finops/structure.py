"""
Pydantic structure definitions to use in the finops microservice
"""
from datetime import datetime

from pydantic import BaseModel, Field


class CloudBilling(BaseModel):
    start: datetime = Field(description="Start of the period.")
    end: datetime = Field(description="End of the period.")
    value: float = Field(description="Value of the consumption during the period.")
    unit: str = Field(description="Currency of the value, exemple USD for US Dollar.")
