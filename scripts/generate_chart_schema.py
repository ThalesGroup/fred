"""Generate values.schema.json for the Fred Helm chart.

Inlines the three backend configuration JSON schemas (already generated by
generate_config_schema.py) into the correct positions of a hand-written
chart-level schema.  The result is a single self-contained JSON Schema
(draft-07, no $ref) that Helm and IDE plugins can use for validation and
autocompletion.

Usage (called automatically by python-config-schema.mk):
    python scripts/generate_chart_schema.py \\
        --fred-agents        apps/fred-agents/config/schema/configuration.schema.json \\
        --knowledge-flow     apps/knowledge-flow-backend/config/schema/configuration.schema.json \\
        --control-plane      apps/control-plane-backend/config/schema/configuration.schema.json \\
        --output             deploy/charts/fred/values.schema.json
"""

import argparse
import copy
import json
import sys
from pathlib import Path


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _load(path: Path) -> dict:
    with open(path) as f:
        return json.load(f)


def _inline_refs(schema: dict) -> dict:
    """Resolve all $ref pointers within a self-contained schema that uses $defs.

    Uses a `seen` set to break cycles: if a definition is currently being
    expanded, replace the back-reference with an empty object rather than
    looping indefinitely.
    """
    schema = copy.deepcopy(schema)
    defs = schema.get("$defs", {})

    def _resolve(node: object, seen: frozenset[str] = frozenset()) -> object:
        if isinstance(node, dict):
            if "$ref" in node:
                ref = node["$ref"]
                if ref.startswith("#/$defs/"):
                    name = ref[len("#/$defs/"):]
                    if name in seen:
                        return {}  # cycle guard: emit empty object
                    return _resolve(copy.deepcopy(defs[name]), seen | {name})
                return node
            return {k: _resolve(v, seen) for k, v in node.items() if k != "$defs"}
        if isinstance(node, list):
            return [_resolve(i, seen) for i in node]
        return node

    resolved = _resolve(schema)
    resolved.pop("$defs", None)
    return resolved


# ---------------------------------------------------------------------------
# Shared Kubernetes / Helm sub-schemas
# ---------------------------------------------------------------------------

_STRING = {"type": "string"}
_BOOL = {"type": "boolean"}
_INT = {"type": "integer"}
_OBJECT_FREE = {"type": "object"}  # used only where the shape is truly open (annotations, labels)
_ANY = {}


def _obj(props: dict, required: list[str] | None = None, additional: bool = False) -> dict:
    s: dict = {"type": "object", "properties": props, "additionalProperties": additional}
    if required:
        s["required"] = required
    return s


def _arr(items: dict) -> dict:
    return {"type": "array", "items": items}


