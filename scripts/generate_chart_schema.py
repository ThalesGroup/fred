def _fred_agents_app(fa_config: dict) -> dict:
    # models_catalog: free-form (large catalog structure, fully user-defined)
    # mcp_catalog: free-form
    # dotenv: key/value env secrets
    return _obj(_base_app_props({
        "configuration": fa_config,
        "dotenv": _obj({}, additional=True),
        "models_catalog": _obj({}, additional=True),
        "mcp_catalog": _obj({}, additional=True),
    }))


def _frontend_app() -> dict:
    _frontend_config = _obj({
        "config_json": _obj({
            "frontend_basename": _STRING,
        }, additional=True),
    }, additional=True)

    return _obj(_base_app_props({
        "configuration": _frontend_config,
    }))


def _kf_app(kf_config: dict) -> dict:
    return _obj(_base_app_props({
        "configuration": kf_config,
        "dotenv": _obj({}, additional=True),
    }))


def _kf_worker_app(kf_config: dict) -> dict:
    return _obj(_base_app_props({
        "configuration": kf_config,
        "dotenv": _obj({}, additional=True),
    }))


def _cp_app(cp_config: dict) -> dict:
    _policy_entry = _obj({
        "mode": _STRING,
        "retention": _STRING,
        "cancel_on_rejoin": _BOOL,
    }, additional=True)

    _policy_rule = _obj({
        "rule_id": _STRING,
        "match": _obj({
            "team_id": _STRING,
            "trigger": _STRING,
        }, additional=True),
        "action": _obj({
            "retention": _STRING,
        }, additional=True),
    }, additional=True)

    _purge_policy = _obj({
        "default": _policy_entry,
        "rules": _arr(_policy_rule),
    })

    _conversation_policy_catalog = _obj({
        "version": _STRING,
        "conversation_policies": _obj({
            "purge": _purge_policy,
        }, additional=True),
    })

    return _obj(_base_app_props({
        "configuration": cp_config,
        "conversation_policy_catalog": _conversation_policy_catalog,
        "dotenv": _obj({}, additional=True),
    }))


def _cp_worker_app(cp_config: dict) -> dict:
    return _obj(_base_app_props({
        "configuration": cp_config,
        "dotenv": _obj({}, additional=True),
    }))


