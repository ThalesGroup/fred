// Offline-safe Monaco setup.
//
// By default `@monaco-editor/react` (via `@monaco-editor/loader`) downloads the
// editor core from the jsDelivr CDN at runtime, which breaks when the browser is
// offline or behind a strict proxy. Importing this module once — from the lazy
// MonacoPane chunk so Monaco stays out of the main bundle — points the loader at
// the locally bundled `monaco-editor` package and wires its language web workers
// through Vite, so every byte ships inside our own image. No external fetch.

import { loader } from "@monaco-editor/react";
import * as monaco from "monaco-editor";
import editorWorker from "monaco-editor/esm/vs/editor/editor.worker?worker";
import jsonWorker from "monaco-editor/esm/vs/language/json/json.worker?worker";
import cssWorker from "monaco-editor/esm/vs/language/css/css.worker?worker";
import htmlWorker from "monaco-editor/esm/vs/language/html/html.worker?worker";
import tsWorker from "monaco-editor/esm/vs/language/typescript/ts.worker?worker";

self.MonacoEnvironment = {
  getWorker(_workerId, label) {
    switch (label) {
      case "json":
        return new jsonWorker();
      case "css":
      case "scss":
      case "less":
        return new cssWorker();
      case "html":
      case "handlebars":
      case "razor":
        return new htmlWorker();
      case "typescript":
      case "javascript":
        return new tsWorker();
      default:
        return new editorWorker();
    }
  },
};

// Use the bundled instance instead of the CDN. Must run before the Editor mounts.
loader.config({ monaco });
