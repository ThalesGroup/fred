ChatModelHandle: TypeAlias = object
RuntimeToolHandle: TypeAlias = object
CheckpointHandle: TypeAlias = object
SpanPort: TypeAlias = Span
TracerPort: TypeAlias = Tracer


class FrozenModel(BaseModel):
    model_config = ConfigDict(extra="forbid", frozen=True, arbitrary_types_allowed=True)


class CheckpointStrategy(str, Enum):
    SESSION = "session"
    EXCHANGE = "exchange"
    DISABLED = "disabled"


class RuntimeEventKind(str, Enum):
    STATUS = "status"
    TOOL_CALL = "tool_call"
    TOOL_RESULT = "tool_result"
    AWAITING_HUMAN = "awaiting_human"
    ASSISTANT_DELTA = "assistant_delta"
    NODE_ERROR = "node_error"
    FINAL = "final"


class ExecutionConfig(FrozenModel):
    """
    Per-run execution options resolved by the chat/session layer.

    Practical meaning:
    - `thread_id` / `checkpoint_id`: resume the right conversation state.
    - `adapter_config`: optional adapter-specific config passthrough when one runtime
      bridge needs extra execution metadata.
    - `max_steps`: hard stop against runaway loops.
    - `resume_payload`: data provided by a previous HITL pause.
    """

    checkpoint_strategy: CheckpointStrategy = CheckpointStrategy.SESSION
    thread_id: str | None = None
    checkpoint_id: str | None = None
    adapter_config: dict[str, object] = Field(default_factory=dict)
    max_steps: int = Field(default=100, ge=1)
    stream_intermediate_events: bool = True
    resume_payload: object | None = None


class RuntimeEventBase(FrozenModel):
    sequence: int = Field(default=0, ge=0)


class StatusRuntimeEvent(RuntimeEventBase):
    kind: Literal[RuntimeEventKind.STATUS] = RuntimeEventKind.STATUS
    status: str = Field(..., min_length=1)
    detail: str | None = None


class ToolCallRuntimeEvent(RuntimeEventBase):
    kind: Literal[RuntimeEventKind.TOOL_CALL] = RuntimeEventKind.TOOL_CALL
    tool_name: str = Field(..., min_length=1)
    call_id: str = Field(..., min_length=1)
    arguments: dict[str, object] = Field(default_factory=dict)


class ToolResultRuntimeEvent(RuntimeEventBase):
    kind: Literal[RuntimeEventKind.TOOL_RESULT] = RuntimeEventKind.TOOL_RESULT
    call_id: str = Field(..., min_length=1)
    content: str = Field(default="")
    tool_name: str | None = None
    is_error: bool = False
    sources: tuple[VectorSearchHit, ...] = ()
    ui_parts: tuple[UiPart, ...] = ()


class HumanChoiceOption(FrozenModel):
    id: str = Field(..., min_length=1)
    label: str = Field(..., min_length=1)
    description: str | None = None
    default: bool = False


class HumanInputRequest(FrozenModel):
    """
    Structured question shown to the user when runtime needs input.

    Use this model to represent business questions (choice, free text, or
    both). The UI renders this object directly.
    """

    stage: str | None = None
    title: str | None = None
    question: str | None = None
    choices: tuple[HumanChoiceOption, ...] = ()
    free_text: bool = False
    metadata: dict[str, JsonScalar] = Field(default_factory=dict)
    checkpoint_id: str | None = None


class AwaitingHumanRuntimeEvent(RuntimeEventBase):
    kind: Literal[RuntimeEventKind.AWAITING_HUMAN] = RuntimeEventKind.AWAITING_HUMAN
    request: HumanInputRequest


class AssistantDeltaRuntimeEvent(RuntimeEventBase):
    kind: Literal[RuntimeEventKind.ASSISTANT_DELTA] = RuntimeEventKind.ASSISTANT_DELTA
    delta: str = Field(..., min_length=1)


class NodeErrorRuntimeEvent(RuntimeEventBase):
    """
    Emitted when a node handler raises an unhandled exception and the node
    declares `on_error` routing.

    Why this exists:
    - infrastructure failures (MCP server down, network timeout) must be
      distinguishable from normal agent answers at the UI layer
    - the UI can show an inline warning banner instead of displaying the error
      string as if it were a regular answer

    Fields:
    - `node_id`: the node that raised
    - `error_message`: str(exc) — a short technical description
    - `routed_to`: the node the runtime is routing to next (the on_error target)
    """

    kind: Literal[RuntimeEventKind.NODE_ERROR] = RuntimeEventKind.NODE_ERROR
    node_id: str = Field(..., min_length=1)
    error_message: str = Field(..., min_length=1)
    routed_to: str = Field(..., min_length=1)


class FinalRuntimeEvent(RuntimeEventBase):
    kind: Literal[RuntimeEventKind.FINAL] = RuntimeEventKind.FINAL
    content: str = Field(default="")
    sources: tuple[VectorSearchHit, ...] = ()
    ui_parts: tuple[UiPart, ...] = ()
    model_name: str | None = None
    token_usage: dict[str, int] | None = None
    finish_reason: str | None = None


RuntimeEvent: TypeAlias = Annotated[
    StatusRuntimeEvent
    | ToolCallRuntimeEvent
    | ToolResultRuntimeEvent
    | AwaitingHumanRuntimeEvent
    | AssistantDeltaRuntimeEvent
    | NodeErrorRuntimeEvent
    | FinalRuntimeEvent,
    Field(discriminator="kind"),
]


class TokenProviderPort(ABC):
    @abstractmethod
    def get_bearer_token(self, binding: BoundRuntimeContext) -> str:
        """Return a bearer token appropriate for the currently bound runtime."""


class ChatModelFactoryPort(ABC):
    @abstractmethod
    def build(
        self, definition: AgentDefinition, binding: BoundRuntimeContext
    ) -> ChatModelHandle:
        """
        Resolve the canonical chat model for the currently bound agent runtime.

        This port exists so v2 runtimes stay platform-owned and testable:
        agent definitions remain pure declarations, while model selection stays
        an injected capability that Fred can later align with a broader SDK.
        """

    def build_for_operation(
        self,
        *,
        definition: AgentDefinition,
        binding: BoundRuntimeContext,
        purpose: str,
        operation: str | None,
    ) -> ChatModelHandle | None:
        """
        Optionally resolve a chat model for a specific operation within one turn.

        Why this exists:
        - advanced routing factories (e.g. RoutedChatModelFactory) can select
          different models for different phases of a ReAct/Graph turn (e.g.
          "routing" vs "planning" vs "answering")
        - base implementations return None to signal "use build() instead"

        How to use it:
        - runtimes call this first; fall back to build() when None is returned
        - do not override if the factory does not support per-operation routing
        """
        return None


class ToolInvokerPort(ABC):
    @abstractmethod
    async def invoke(self, request: ToolInvocationRequest) -> ToolInvocationResult:
        """Invoke a tool through the platform's transport layer."""


class AgentInvokerPort(ABC):
    """
    Platform port for invoking a registered fred v2 agent from a graph node.

    Why this port exists:
    - graph nodes should be able to call other agents without knowing whether
      the target runs in-process, as a Temporal child workflow, or over HTTP
    - this port is the single extension point; concrete implementations are
      injected by the runtime, not the agent author

    How to implement it:
    - provide one concrete subclass per deployment strategy
      (e.g. ``TemporalAgentInvoker``, ``LocalAgentInvoker``)
    - the implementation is responsible for agent lookup, execution, and
      mapping the agent's output to ``AgentInvocationResult``

    How to use it:
    - do not call this port directly from agent code; use
      ``GraphNodeContext.invoke_agent`` instead
    """

    @abstractmethod
    async def invoke(self, request: AgentInvocationRequest) -> AgentInvocationResult:
        """
        Run a registered agent for one turn and return its output.

        The implementation decides the execution strategy (in-process, child
        workflow, remote call). It must populate ``AgentInvocationResult`` with
        at least ``content`` and ``is_error``.
        """


class ToolProviderPort(ABC):
    @abstractmethod
    def bind(self, binding: BoundRuntimeContext) -> None:
        """Refresh context-scoped tool-provider state for the current runtime."""

    @abstractmethod
    async def activate(self) -> None:
        """Initialize any heavy provider resources before tool execution."""

    @abstractmethod
    def get_tools(self) -> tuple[RuntimeToolHandle, ...]:
        """Return the current runtime-provided tool set."""

    @abstractmethod
    async def aclose(self) -> None:
        """Release provider resources."""


class ArtifactPublisherPort(ABC):
    @abstractmethod
    def bind(self, binding: BoundRuntimeContext) -> None:
        """Refresh context-scoped publishing state for the current runtime."""

    @abstractmethod
    async def publish(self, request: ArtifactPublishRequest) -> PublishedArtifact:
        """Store a generated artifact and return its downloadable description."""


class ResourceReaderPort(ABC):
    @abstractmethod
    def bind(self, binding: BoundRuntimeContext) -> None:
        """Refresh context-scoped resource access state for the current runtime."""

    @abstractmethod
    async def fetch(self, request: ResourceFetchRequest) -> FetchedResource:
        """Read an existing Fred-managed resource such as a template or note."""


class HistoryStorePort(Protocol):
    """
    Port for writing and reading conversation history from a durable store.

    Why this port exists:
    - ``fred-sdk`` must not import from ``fred-core`` (layer rule), so this
      protocol defines the interface using loose ``Any`` types for messages
    - the concrete implementation (``fred_core.history.PostgresHistoryStore``)
      is injected at runtime by ``fred-runtime``; the sdk-level runtime never
      needs to know the message type

    How to implement it:
    - provide ``fred_core.history.PostgresHistoryStore`` as the concrete impl
    - inject via ``RuntimeServices.history_store``
    - the concrete type for ``messages`` is ``list[ChatMessage]`` at call sites

    How to use it:
    - runtimes or adapters that write history call ``save`` once per turn
    - ``get`` is called by pod endpoints to reconstruct the message timeline
    - ``list_sessions`` is called by the session-listing endpoint
    """

    async def save(
        self,
        session_id: str,
        messages: List[Any],
        user_id: str,
    ) -> None:
        """Persist a batch of messages for one turn."""
        ...

    async def get(
        self,
        session_id: str,
    ) -> List[Any]:
        """Retrieve all messages for a session, ordered by rank ascending."""
        ...

    async def list_sessions(
        self,
        user_id: str,
    ) -> List[str]:
        """Return distinct session IDs for a user, most recent first."""
        ...


@dataclass(frozen=True, slots=True)
class RuntimeServices:
    """
    Dependencies injected into a runtime instance.

    Think of this as the runtime toolbox:
    - model factory
    - tool invocation/provider
    - artifact publish/read
    - tracing and KPI
    - checkpointer
    - history store (durable conversation history, independent from the checkpointer)

    In production, most debugging starts by checking which services were
    actually injected for a given runtime.
    """

    tracer: TracerPort | None = None
    chat_model_factory: ChatModelFactoryPort | None = None
    token_provider: TokenProviderPort | None = None
    tool_invoker: ToolInvokerPort | None = None
    tool_provider: ToolProviderPort | None = None
    agent_invoker: AgentInvokerPort | None = None
    artifact_publisher: ArtifactPublisherPort | None = None
    resource_reader: ResourceReaderPort | None = None
    metrics: MetricsProvider | None = None
    checkpointer: CheckpointHandle | None = None
    history_store: HistoryStorePort | None = None


InputModelT = TypeVar("InputModelT", bound=BaseModel)
OutputModelT = TypeVar("OutputModelT", bound=BaseModel)
DefinitionT = TypeVar("DefinitionT", bound=AgentDefinition)


class Executor(ABC, Generic[InputModelT, OutputModelT]):
    @abstractmethod
    async def invoke(
        self, input_model: InputModelT, config: ExecutionConfig
    ) -> OutputModelT:
        """Execute the agent once and return the final output."""

    @abstractmethod
    def stream(
        self, input_model: InputModelT, config: ExecutionConfig
    ) -> AsyncIterator[RuntimeEvent]:
        """Execute the agent and yield runtime events."""


class AgentRuntime(ABC, Generic[DefinitionT, InputModelT, OutputModelT]):
    """
    Base lifecycle shared by concrete runtimes (ReAct/Graph).

    Lifecycle to remember:
    - `bind(...)`: cheap context switch (session/user/token).
    - `activate()`: one-time heavy initialization.
    - `get_executor()`: returns the executable engine.

    If a runtime behaves oddly across sessions, inspect `bind()`/`on_bind()`
    first; if it leaks resources, inspect `dispose()`/`on_dispose()`.
    """

    def __init__(self, *, definition: DefinitionT, services: RuntimeServices):
        self._definition = definition
        self._services = services
        self._binding: BoundRuntimeContext | None = None
        self._activated = False
        self._disposed = False
        self._executor: Executor[InputModelT, OutputModelT] | None = None

    @property
    def definition(self) -> DefinitionT:
        return self._definition

    @property
    def services(self) -> RuntimeServices:
        return self._services

    @property
    def binding(self) -> BoundRuntimeContext:
        if self._binding is None:
            raise RuntimeError("AgentRuntime is not bound. Call bind() first.")
        return self._binding

    @property
    def is_activated(self) -> bool:
        return self._activated

    @property
    def is_disposed(self) -> bool:
        return self._disposed

    def bind(self, binding: BoundRuntimeContext) -> None:
        if self._disposed:
            raise RuntimeError("Cannot bind a disposed AgentRuntime.")

        self._binding = binding
        self._executor = None
        self.on_bind(binding)

    def on_bind(self, binding: BoundRuntimeContext) -> None:
        """Optional hook for subclasses to rebuild context-scoped helper state."""

    async def activate(self) -> None:
        if self._disposed:
            raise RuntimeError("Cannot activate a disposed AgentRuntime.")
        if self._activated:
            return
        binding = self.binding
        await self.on_activate(binding)
        self._activated = True

    async def on_activate(self, binding: BoundRuntimeContext) -> None:
        """Optional hook for one-time heavy activation logic."""

    async def get_executor(self) -> Executor[InputModelT, OutputModelT]:
        if self._disposed:
            raise RuntimeError("Cannot build an executor from a disposed AgentRuntime.")

        await self.activate()
        binding = self.binding
        if self._executor is None:
            self._executor = await self.build_executor(binding)
        return self._executor

    @abstractmethod
    async def build_executor(
        self, binding: BoundRuntimeContext
    ) -> Executor[InputModelT, OutputModelT]:
        """Return the canonical executor for the currently bound runtime."""

    async def dispose(self) -> None:
        if self._disposed:
            return
        try:
            await self.on_dispose()
        finally:
            self._disposed = True
            self._executor = None
            self._binding = None

    async def on_dispose(self) -> None:
        """Optional hook for resource cleanup."""
