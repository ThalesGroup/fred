"""
Runtime primitives used by all v2 agents.

Read this file when you need to understand three things quickly:
1. Which events the runtime can emit (`RuntimeEvent*`).
2. Which platform capabilities a runtime can use (`RuntimeServices` ports).
3. What lifecycle a runtime implementation must follow (`AgentRuntime`).

If you are authoring an agent, you usually do not edit this file. You edit a
definition (`ReActAgentDefinition` or `GraphAgentDefinition`) and let runtime
implementations execute it.
"""

from __future__ import annotations

from abc import ABC, abstractmethod
